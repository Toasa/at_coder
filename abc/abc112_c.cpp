// Pyramid

#include <iostream>
#include <cmath>
#include <vector>
#include <algorithm>
#include <string>

using namespace std;

int main() {
    int n;

    vector<int> x(n);
    vector<int> y(n);
    vector<int> h(n);

    for (int i = 0; i < n; i++) {
        cin >> x[i] >> y[i] >> h[i];
    }


    return 0;
}
